package org.jdataex.channel.exception;

public class HandlerException extends BaseNestedException{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8932788026722304892L;
	
	
	private int errorCode;
	
	private int handlerPosition;

	public int getHandlerPosition() {
		return handlerPosition;
	}

	public void setHandlerPosition(int handlerPosition) {
		this.handlerPosition = handlerPosition;
	}

	public int getErrorCode() {
		return errorCode;
	}

	public void setErrorCode(int errorCode) {
		this.errorCode = errorCode;
	}

	public HandlerException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public HandlerException(String msg) {
		super(msg);
	}
}
