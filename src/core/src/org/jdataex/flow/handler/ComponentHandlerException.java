package org.jdataex.flow.handler;

import org.jdataex.core.JDataExException;

public class ComponentHandlerException extends JDataExException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4033854666869944535L;

	public ComponentHandlerException() {
		super();
	}

	public ComponentHandlerException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ComponentHandlerException(String message, Throwable cause) {
		super(message, cause);
	}

	public ComponentHandlerException(String message) {
		super(message);
	}

	public ComponentHandlerException(Throwable cause) {
		super(cause);
	}

}
