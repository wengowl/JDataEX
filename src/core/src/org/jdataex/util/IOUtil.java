package org.jdataex.util;

import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;

public class IOUtil {

	
	public static void closeQueit(InputStream input){
		if(input != null){
			try {
				input.close();
			} catch (IOException e) {
			}
		}
	}
	
	public static void closeQueit(OutputStream output){
		if(output != null){
			try {
				output.close();
			} catch (IOException e) {
			}
		}
	}
}
