package org.jdataex.util.logger;

/**
 * 日志功能接口。通过这个接口容器可以分配指定的日志实例，实现分类记录。
 * @author TwelveFoldEr
 *
 */
public interface ILoggable {

	/**
	 * 设置日志实例。通过这个方法指定具体的日志实例。
	 * @param logger 指定的日志实例。
	 */
	public void setLogger(ILogger logger);
	
	/**
	 * 获取日志实例。
	 * @return logger 日志实例。
	 */
	public ILogger getLogger();
	
}
