package org.jdataex.core;

public class ReleaseException extends ResourceException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4932705036816527869L;

	public ReleaseException() {
		super();
	}

	public ReleaseException(String msg) {
		super(msg);
	}

	public ReleaseException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ReleaseException(String message, Throwable cause) {
		super(message, cause);
	}

	public ReleaseException(Throwable cause) {
		super(cause);
	}

}
