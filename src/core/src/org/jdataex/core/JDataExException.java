package org.jdataex.core;

public class JDataExException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 8908432833717981305L;

	public JDataExException() {
		super();
	}

	public JDataExException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public JDataExException(String message, Throwable cause) {
		super(message, cause);
	}

	public JDataExException(String message) {
		super(message);
	}

	public JDataExException(Throwable cause) {
		super(cause);
	}

}
